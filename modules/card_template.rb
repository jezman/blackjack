module CardTemplate
  def face_card(rank, suite)
    [
      '┌─────────┐',
      '│%2s       │' % rank,
      '│         │',
      '│         │',
      '│ %4s    │' % suite,
      '│         │',
      '│         │',
      '│       %-2s│' % rank,
      '└─────────┘'
    ]
  end

  def hidden_card
    %w[
      ┌─────────┐
      │░░░░░░░░░│
      │░░░░░░░░░│
      │░░░░░░░░░│
      │░░░░░░░░░│
      │░░░░░░░░░│
      │░░░░░░░░░│
      │░░░░░░░░░│
      └─────────┘
    ]
  end
end
